public class Main {
    public static void main(String[] args){
        // Data types: In java, there is two types of data. primitive and non-primitive
        // primitive data are int, String, boolean, float, double
        // non-primitive data is objects
        String name = "Noyon Sarker";
        int marks = 80;
        float cgpa = 4.50f;
        boolean isLoggedIn = true;
        double point = 25.30d;
        System.out.println("Name: "+name);
        System.out.println("Marks: "+marks);
        System.out.println("CGPA: "+cgpa);
        System.out.println("Boolean: "+isLoggedIn);
        System.out.println("Point: "+point);
    }
}